import java.sql.SQLOutput;

public class Increment_Dicrement {
    public static void main(String[] args) {

        int a = 4;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        int b = 4;
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);

        char c = 'A';
        System.out.println(c++);
        System.out.println(c);
        System.out.println(++c);


    }
}
