public class Loops {
    public static void main(String[] args) {

        // while

        int i = 0;
        while (i<=3){
            System.out.println(i);
            i++;
        }

        // do while

        int q = 0;
        do {
            System.out.println(q);
            q++;
        }while(q<=3);

        //for
        int n = 19;
        for (int a = n; a>0; a--){
            System.out.println(a);
        }
    }
}
